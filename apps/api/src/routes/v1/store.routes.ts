import express, { Router } from 'express';

import { validate } from '@/common/validate';
import { auth } from '@/modules/auth';
import { storeController, storeValidation } from '@/modules/store';

const router: Router = express.Router();

router
  .route('/')
  .post(auth(), validate(storeValidation.createStore), storeController.createStore)
  .get(auth(), validate(storeValidation.getStores), storeController.getStores);

router
  .route('/:storeId')
  .get(auth(), validate(storeValidation.getStore), storeController.getStore)
  .patch(auth(), validate(storeValidation.updateStore), storeController.updateStore)
  .delete(auth(), validate(storeValidation.deleteStore), storeController.deleteStore);

export default router;

